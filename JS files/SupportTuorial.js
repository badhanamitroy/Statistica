//Arithmatic Mean
let AMtutLink = document.querySelector("#AMtut");
let isAMhidden = true;
function showHidAMtut(){

    if(isAMhidden){
        AMtutLink.style.display = "none";
        isAMhidden = false;
    }
    else{
        AMtutLink.style.display = "block";
        isAMhidden = true;
    }
}

